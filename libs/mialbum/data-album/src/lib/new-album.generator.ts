function newTeam(category: FigureCategory, prefix: FigurePrefix): Figure[] {
  const teamArray: Figure[] = [];
  for (let number = 1; number < 20; number++) {
    teamArray.push({
      section: FigureSection.TEAM,
      checked: false,
      category,
      prefix,
      type: number === 1 ? FigureType.EMBLEM : FigureType.PLAYER,
      number,
    });
  }
  return teamArray;
}
