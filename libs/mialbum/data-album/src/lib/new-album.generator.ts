export function generateNewAlbum(
  userId: string,
  name: string,
  key: string
): Album {
  return {
    name,
    dates: {
      start: new Date().getTime(),
      lastUpdate: new Date().getTime(),
    },
    managers: {
      [userId]: OwnerRol.CREATOR,
    },
    key,
    owner: userId,
    figures: generateNewAlbumFigures(),
  };
}

